public class Practice {
	public static void main(String[] args) {
		  String number="hello";  
		    //Switch expression  
		    switch(number){  
		    //Case statements  
		    case "hii": System.out.println("10");  
		    break;  
		    case "hello": System.out.println("20 long");  
		    break;  
		    case "b": System.out.println("30");  
		    break;  
		    //Default case statement  
		    default:System.out.println("Not in 10, 20 or 30");  
		    }  
		
		}
	}


